// outer class
class OuterClass {

    String name;

    void setName() {
        this.name = name;
    }

    String getName() {
        return this.name;
    }

    class InnerClass {

        int age;

        void setAge(int age) {
            this.age = age;
        }

        int getAge() {
            return this.age;
        }
    }
}
